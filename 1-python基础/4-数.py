# 数分为整数和浮点数
# 整数可以进行+ - * / ** % 等运算
print(2 + 3)
print(2 - 3)
print(2 * 3)
print(30 / 2)
print(3 % 2)
print(3 ** 2)
# 有些浮点数无法进行精确计算
print(0.2 + 0.1)

# 任意两个数相除，结果都为浮点数，即使都是浮点数且可以整除
# 有一个数为浮点数，运算结果总是浮点数
# 数字很大，可以在数字中间加上下划线
age = 1_400_000_000
print(age)

# 同时给多个变量赋值(变量和值的个数要相同)
x, y, z = 0, 1, 2
print(x)
print(y)
print(z)